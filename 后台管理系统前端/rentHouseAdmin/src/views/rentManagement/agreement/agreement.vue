      { status: AgreementStatus.SIGNED, count: data.signed || 0, label: '已签约' },
      { status: AgreementStatus.WAITING, count: data.signing || 0, label: '签约待确认' },
      { status: AgreementStatus.CANCELLED, count: data.canceled || 0, label: '已取消' },
    ].filter(item => item.count > 0) // 只显示有数据的状态
    
    statistics.sourceStats = [
      { sourceType: 1, count: data.newLease || 0, label: '新签' },
      { sourceType: 2, count: data.renewLease || 0, label: '续约' },
      { sourceType: 3, count: data.directApplication || 0, label: '直接入住申请' },
    ].filter(item => item.count > 0) // 只显示有数据的来源
    
    // 更新图表
    await nextTick()
    initCharts()
  } catch (error) {
    console.error('加载统计数据失败:', error)
  }
}

