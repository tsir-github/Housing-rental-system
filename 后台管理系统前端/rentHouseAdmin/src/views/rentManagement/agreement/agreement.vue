// 计算属性：根据状态获取数量
const getStatusCount = (status: number) => {
  // 直接从后端返回的数据中获取对应状态的数量
  switch (status) {
    case AgreementStatus.SIGNED:
      return statistics.signed || 0
    case AgreementStatus.WAITING:
      return statistics.signing || 0
    case AgreementStatus.CANCELLED:
      return statistics.canceled || 0
    default:
      return 0
  }
}

// 计算属性：自动生成的租约数量
const autoGeneratedCount = computed(() => {
  return statistics.autoGenerated || 0
})

