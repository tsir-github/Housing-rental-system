<template>
  <div 
    class="lease-card"
    :class="{ 'expiring': isExpiring }"
    @click="$emit('click', lease)"
  >
    <!-- 租约状态标签 -->
    <div class="lease-status-badge">
      <van-tag 
        :color="getLeaseStatusColor(lease.status)"
        plain
        size="medium"
      >
        {{ getLeaseStatusName(lease.status) }}
      </van-tag>
    </div>

    <!-- 租约基本信息 -->
    <div class="lease-header">
      <div class="lease-title">
        {{ lease.apartmentInfo.name }} - {{ lease.roomInfo.roomNumber }}
      </div>
      <div class="lease-location">
        {{ lease.apartmentInfo.provinceName }}{{ lease.apartmentInfo.cityName }}{{ lease.apartmentInfo.districtName }}
      </div>
    </div>

    <!-- 租客信息 -->
    <div class="lease-tenant">
      <div class="tenant-info">
        <van-icon name="contact" size="16" />
        <span class="tenant-name">{{ lease.name }}</span>
        <span class="tenant-phone">{{ lease.phone }}</span>
      </div>
    </div>

    <!-- 租约详情 -->
    <div class="lease-details">
      <div class="detail-row">
        <div class="detail-item">
          <span class="detail-label">租金</span>
          <span class="detail-value rent">¥{{ lease.rent }}/月</span>
        </div>
        <div class="detail-item">
          <span class="detail-label">押金</span>
          <span class="detail-value">¥{{ lease.deposit }}</span>
        </div>
      </div>
      <div class="detail-row">
        <div class="detail-item">
          <span class="detail-label">租期</span>
