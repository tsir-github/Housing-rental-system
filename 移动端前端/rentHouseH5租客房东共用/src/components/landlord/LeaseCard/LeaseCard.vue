        </div>
        <div class="detail-item">
          <span class="detail-label">支付方式</span>
          <span class="detail-value">{{ lease.paymentType.name }}</span>
        </div>
      </div>
    </div>

    <!-- 租约时间 -->
    <div class="lease-time">
      <div class="time-item">
        <span class="time-label">开始时间</span>
        <span class="time-value">{{ formatDate(lease.leaseStartDate) }}</span>
      </div>
      <div class="time-item">
        <span class="time-label">结束时间</span>
        <span class="time-value">{{ formatDate(lease.leaseEndDate) }}</span>
      </div>
      <div class="time-item" v-if="lease.remainingDays > 0">
        <span class="time-label">剩余天数</span>
        <span class="time-value" :class="{ 'expiring': lease.remainingDays <= 30 }">
          {{ lease.remainingDays }}天
        </span>
      </div>
    </div>

    <!-- 租约来源和类型 -->
    <div class="lease-meta">
      <van-tag size="small" plain>
        {{ getLeaseSourceTypeName(lease.sourceType) }}
      </van-tag>
      <van-tag size="small" plain v-if="lease.autoGenerated">
        自动生成
      </van-tag>
      <span class="create-time">{{ formatDateTime(lease.createTime) }}</span>
    </div>

    <!-- 即将到期提醒 -->
    <div v-if="isExpiring" class="expiring-notice">
      <van-notice-bar
        color="#ff9500"
        background="#fff7cc"
        left-icon="warning-o"
        :scrollable="false"
      >
        {{ lease.remainingDays }}天后到期
      </van-notice-bar>
    </div>
  </div>
</template>

<script setup lang="ts">
import { computed } from 'vue';
import {
  getLeaseStatusName,
  getLeaseStatusColor,
  getLeaseSourceTypeName,
  type LandlordLeaseVo
} from '@/api/landlord/lease';

// Props
interface Props {
  lease: LandlordLeaseVo;
  showExpiring?: boolean;
}

const props = withDefaults(defineProps<Props>(), {
  showExpiring: true
});

// Emits
defineEmits<{
  click: [lease: LandlordLeaseVo];
}>();

// 计算属性
const isExpiring = computed(() => {
  return props.showExpiring && props.lease.remainingDays > 0 && props.lease.remainingDays <= 30;
});

// 格式化日期
const formatDate = (dateStr: string): string => {
  return new Date(dateStr).toLocaleDateString();
};

const formatDateTime = (dateStr: string): string => {
  return new Date(dateStr).toLocaleString();
};
</script>

